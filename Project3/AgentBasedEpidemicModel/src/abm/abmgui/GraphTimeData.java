package abm.abmgui;

public class GraphTimeData {

    private int I;
    private int S;
    private int R;

    public GraphTimeData(int I, int S, int R){
        this.I = I;
        this.S = S;
        this.R = R;
    }

    public int getI() { return I; }

    public int getS() {
        return S;
    }

    public int getR() { return R; }
}
